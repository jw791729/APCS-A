class Switch
{
    public static void main(){
        
        int age;
        age = 2;
        
        switch (age) {
        case 1:
            System.out.println("You can work in a sweat shop.");
            break;
        case 2:
            System.out.println("You can manage a sweat shop.");
            break;
        case 3:
            System.out.println("You can get this bread.");
            break;
        case 4:
            System.out.println("You can eat bread.");
            break;
        default:
            System.out.println("Who is you bruh.");
        break;
        
        
    }
}
}